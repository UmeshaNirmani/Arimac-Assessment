<template>
  <container fluid class="main">
    <div class="addBtn">
      <router-link to="/add" type="button" class="add btn btn-success"
        >Add User</router-link
      >
    </div>
    <div class="card-group">
      <div class="row">
        <div
          class="card"
          style="width: 18rem"
          v-for="(person, key) in people"
          :key="key"
        >
          <img
            class="card-img-top"
            src="https://avatars.dicebear.com/v2/avataaars/{{ person.username }}.svg?options[mood][]=happy"
            alt="Card image cap"
          />
          <div class="card-body">
            <h5 class="card-title">
              {{ person.name }}
            </h5>
            <div class="card-text">
              <i class="fa-solid fa-envelope"> </i>
              <p class="icon">
                {{ person.email }}
              </p>
              <br />

              <i class="fa-solid fa-phone"></i>
              <p class="icon">
                {{ person.phone }}
              </p>
              <br />

              <i class="fa-solid fa-globe"></i>
              <p class="icon">
                {{ person.website }}
              </p>
            </div>
            <hr />
            <p id="card-text">
              <i class="fa-solid fa-heart"></i>
              <i class="fa-solid fa-pen-to-square"></i>
              <i class="fa-solid fa-trash-can"></i>
            </p>
          </div>
        </div>
      </div>
    </div>
  </container>
</template>

<script>
import UserService from "../services/users";

export default {
  name: "HomeView",
  data: function () {
    return {
      loading: false,
      people: [],
      errorMessage: null,
    };
  },
  created: async function () {
    try {
      this.loading = true;
      let response = await UserService.getAllUsers();
      this.people = response.data;
      this.loading = false;
    } catch (error) {
      this.errorMessage = error;
      this.loading = false;
    }
  },
};
</script>

